void	down14(int *argv, int *arr, int i)
{
	if (*argv == 1)
	{
		arr[i * 4 + 3] = '4';
		if (*(argv + 4) == 3)
		{
			arr[i * 4 + 1] = '3';
		}
	}
	else if (*argv == 4)
	{
		arr[i * 4 + 0] = '4';
		arr[i * 4 + 1] = '3';
		arr[i * 4 + 2] = '2';
		arr[i * 4 + 3] = '1';
	}
}

void	left14(int *argv, int *arr, int i)
{
	if (*argv == 1)
	{
		arr[3 * 4 + i] = '4';
		if (*(argv + 4) == 3)
		{
			arr[i + 4 * 1] = '3';
		}
	}
	else if (*argv == 4)
	{
		arr[0 * 4 + i] = '1';
		arr[1 * 4 + i] = '2';
		arr[2 * 4 + i] = '3';
		arr[3 * 4 + i] = '4';
	}
}
