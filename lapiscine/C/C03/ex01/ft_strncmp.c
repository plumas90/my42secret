int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	a;

	a = 0;
	while ((*s1 != '\0' || *s2 != '\0') && a < n)
	{
		if (*s1 > *s2)
		{
			return (1);
		}
		else if (*s1 < *s2)
		{
			return (-1);
		}
		++a;
		++s1;
		++s2;
	}
	return (0);
}
