void	up13(int *argv, int *arr, int i)
{
	if (*argv == 1)
	{
		arr[i * 4 + 0] = '4';
		if (*(argv + 4) == 3)
		{
			arr[i * 4 + 2] = '3';
		}
	}
	else if (*argv == 3)
	{
		if (*(argv + 4) == 2)
		{
			arr[i * 4 + 2] = '4';
			if (arr[i * 4 + 3] != '0' && arr[i * 4 + 3] != '1')
				arr[i * 4 + 0] = '1';
			else if (arr[i * 4 + 0] != '0' && arr[i * 4 + 0] != '1')
				arr[i * 4 + 3] = '1';
		}
	}
}

void	up24(int *argv, int *arr, int i)
{
	if (*argv == 2)
	{
		if (*(argv + 4) == 2)
		{
			if (arr[i * 4 + 3] != '0' && arr[i * 4 + 3] != '3')
				arr[i * 4 + 0] = '3';
			else if (arr[i * 4 + 0] != '0' && arr[i * 4 + 0] != '3')
				arr[i * 4 + 3] = '3';
		}
		else if (*(argv + 4) == 3)
		{
			if (arr[i * 4 + 3] != '0' && arr[i * 4 + 3] != '1')
				arr[i * 4 + 0] = '1';
			else if (arr[i * 4 + 3] != '0' && arr[i * 4 + 3] != '1')
				arr[i * 4 + 3] = '1';
		}
	}
	else if (*argv == 4)
	{
		arr[i * 4 + 0] = '1';
		arr[i * 4 + 1] = '2';
		arr[i * 4 + 2] = '3';
		arr[i * 4 + 3] = '4';
	}
}
